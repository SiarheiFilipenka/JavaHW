package hw05;

public class Task1 {
    public static void main(String[] args) {

        for (int i = 1; i <= 100; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
        int j = 1;

        while (j <= 100) {
            System.out.print(j + " ");
            j++;
        }
    }
}
