package hw05;

public class Task2 {
    public static void main(String[] args) {

        for (int i = 100; i >= 1; i--) {
            System.out.print(i + " ");
        }
        System.out.println();
        int j = 100;
        while (j >= 1) {
            System.out.print(j + " ");
            j--;
        }
    }
}
