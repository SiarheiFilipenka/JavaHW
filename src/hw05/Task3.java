package hw05;

public class Task3 {
    public static void main(String[] args) {

        for (int i = 20; i >= 1; i -= 2) {
            System.out.print(i + " ");
        }
        System.out.println();
        int j = 20;
        while (j >= 1) {
            System.out.print(j + " ");
            j -= 2;
        }
    }
}
